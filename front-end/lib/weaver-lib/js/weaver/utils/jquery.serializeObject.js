define([
  'jquery'
], function ($) {
  "use strict";

  /**
   *
   * jQuery plugin - a helper to serialize form inputs.
   * @module weaver
   * @submodule utils
   * @namespace utils
   * @method serializeObject
   * @returns {{}}
   */
  $.fn.serializeObject = function () {
    var o = {};
    var a = this.serializeArray();
    $.each(a, function () {
      if (o[this.name] !== undefined) {
        if (!o[this.name].push) {
          o[this.name] = [o[this.name]];
        }
        o[this.name].push(this.value || '');
      } else {
        o[this.name] = this.value || '';
      }
    });
    return o;
  };

});
